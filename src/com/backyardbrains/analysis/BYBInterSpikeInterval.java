package com.backyardbrains.analysis;

public class BYBInterSpikeInterval {

    private final float x;
    private final int y;

    public BYBInterSpikeInterval(float x, int y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public int getY() {
        return y;
    }
};